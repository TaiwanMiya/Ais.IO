#!/bin/bash

# ANSI 控制碼的顏色和樣式測試腳本，用於在終端中顯示不同顏色和樣式。

# 顯示所有 ANSI 控制碼開頭的顏色和模式

echo -e "\n顯示各種 ANSI 控制碼的樣式和顏色："

styles=(
    "0"  # 重置所有屬性
    "1"  # 粗體/亮色
    "2"  # 減淡/暗淡
    "3"  # 斜體（部分終端不支持）
    "4"  # 下劃線
    "5"  # 閃爍（部分終端不支持）
    "6"  # 快速閃爍（已棄用，部分終端不支持）
    "7"  # 反顯（前景和背景顏色互換）
    "8"  # 隱藏（使文字不可見）
    "9"  # 刪除線
)

colors=(
    "30" # 黑色
    "31" # 紅色
    "32" # 綠色
    "33" # 黃色
    "34" # 藍色
    "35" # 紫色
    "36" # 青色
    "37" # 白色
)

for style in "${styles[@]}"; do
    for color in "${colors[@]}"; do
        echo -ne "\033[${style};${color}m${style};${color}\t\033[0m"
    done
    echo ""
done

# 顯示 256 色顏色表
echo -e "\n顯示 256 色顏色表："
for i in {0..255}; do
    # 每行顯示 8 種顏色
    if (( i % 8 == 0 )); then
        echo ""
    fi
    echo -ne "\033[38;5;${i}m${i}\t"
done

echo -e "\033[0m\n"

